# tuple methods

print("Merging two tuples together : ")
tup = (3, 7, 2, 0)
print("tup = ", tup)
lol= (4,7,3,9)
print("lol = ", lol)
arv = tup + lol
print(arv)

print("Count the number of occurence : ")
print(arv.count(3))

print("First index of the desired element : ")
print(arv.index(0))

print("Length of tuple : ")
print(len(arv))
